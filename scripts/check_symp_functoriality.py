from __future__ import annotations
from fractions import Fraction
from collections import defaultdict


# ---------------------------------------------------------------------------
# Formal symplectic structure on $(\widehat{\C^2}_0, \omega = dz_1 \wedge dz_2)$
# ---------------------------------------------------------------------------
#
# Monomials are represented as tuples (p, q) for z_1^p z_2^q.
# Polynomials are dicts {(p, q): Fraction}.
# We track Hamiltonian vector fields, Poisson brackets, and pull-back
# actions exactly via Fraction arithmetic.

def poly_zero():
    return {}

def poly_add(f, g):
    out = defaultdict(Fraction)
    for k, v in f.items():
        out[k] += v
    for k, v in g.items():
        out[k] += v
    return {k: v for k, v in out.items() if v != 0}

def poly_scale(f, alpha):
    a = Fraction(alpha)
    return {k: a * v for k, v in f.items() if a * v != 0}

def poly_mul(f, g):
    out = defaultdict(Fraction)
    for (p1, q1), c1 in f.items():
        for (p2, q2), c2 in g.items():
            out[(p1 + p2, q1 + q2)] += c1 * c2
    return {k: v for k, v in out.items() if v != 0}

def poly_truncate(f, max_total_deg):
    return {(p, q): c for (p, q), c in f.items() if p + q <= max_total_deg}

def poly_pow(f, n, trunc_deg=None):
    out = {(0, 0): Fraction(1)}
    for _ in range(n):
        out = poly_mul(out, f)
        if trunc_deg is not None:
            out = poly_truncate(out, trunc_deg)
    return out


# ---------------------------------------------------------------------------
# Poisson bracket {f, g} = ∂_{z_1} f · ∂_{z_2} g − ∂_{z_2} f · ∂_{z_1} g
# ---------------------------------------------------------------------------

def d_z1(f):
    out = {}
    for (p, q), c in f.items():
        if p > 0:
            out[(p - 1, q)] = c * p
    return out

def d_z2(f):
    out = {}
    for (p, q), c in f.items():
        if q > 0:
            out[(p, q - 1)] = c * q
    return out

def poisson(f, g):
    return poly_add(
        poly_mul(d_z1(f), d_z2(g)),
        poly_scale(poly_mul(d_z2(f), d_z1(g)), -1)
    )


# ---------------------------------------------------------------------------
# Pull-back $\varphi^*$ for a polynomial map $\varphi: (z_1, z_2) \mapsto
# (\Phi_1(z_1, z_2), \Phi_2(z_1, z_2))$ given as (Phi_1, Phi_2) polynomials.
# Truncates to total degree $\leq$ trunc_deg.
# ---------------------------------------------------------------------------

def pullback(f, Phi_1, Phi_2, trunc_deg=10):
    r"""Return $\varphi^* f$ where $\varphi^*(z_1) = \Phi_1$, $\varphi^*(z_2) = \Phi_2$."""
    out = poly_zero()
    for (p, q), c in f.items():
        term_1 = poly_pow(Phi_1, p, trunc_deg=trunc_deg)
        term_2 = poly_pow(Phi_2, q, trunc_deg=trunc_deg)
        prod = poly_mul(term_1, term_2)
        prod = poly_truncate(prod, trunc_deg)
        out = poly_add(out, poly_scale(prod, c))
    return out


# ---------------------------------------------------------------------------
# Symplectic test: $\varphi^* \omega = \omega$ iff $\det(D\varphi) = 1$
# in formal series. We test by computing the Jacobian determinant
# numerically up to truncation.
# ---------------------------------------------------------------------------

def jacobian_det(Phi_1, Phi_2, trunc_deg=10):
    """det(D\varphi) = ∂_{z_1} Phi_1 · ∂_{z_2} Phi_2 - ∂_{z_2} Phi_1 · ∂_{z_1} Phi_2."""
    a = poly_mul(d_z1(Phi_1), d_z2(Phi_2))
    b = poly_mul(d_z2(Phi_1), d_z1(Phi_2))
    return poly_truncate(poly_add(a, poly_scale(b, -1)), trunc_deg)


def is_symplectic(Phi_1, Phi_2, trunc_deg=10):
    det = jacobian_det(Phi_1, Phi_2, trunc_deg)
    one = {(0, 0): Fraction(1)}
    diff = poly_add(det, poly_scale(one, -1))
    return len(diff) == 0


# ---------------------------------------------------------------------------
# Symp_form parabolic test: $\varphi$ preserves $\mathfrak m = (z_1)$ iff
# $\Phi_1 \in (z_1)$ as a formal series (no constant or pure-z_2 terms).
# ---------------------------------------------------------------------------

def preserves_z1_ideal(Phi_1):
    r"""Returns True iff Phi_1 has no terms (0, q) for q ≥ 0, i.e., every
    monomial has z_1-degree at least 1.
    """
    for (p, q), c in Phi_1.items():
        if p == 0 and c != 0:
            return False
    return True


# ---------------------------------------------------------------------------
# Test families: define candidate $\varphi$ for the audit.
# ---------------------------------------------------------------------------

def phi_z2_plus_z1_k(k):
    """φ_k: (z_1, z_2) ↦ (z_1, z_2 + z_1^k), in the parabolic for k ≥ 1
    (z_1 fixed, hence (z_1) preserved).
    """
    Phi_1 = {(1, 0): Fraction(1)}
    Phi_2 = {(0, 1): Fraction(1), (k, 0): Fraction(1)}
    return Phi_1, Phi_2


def phi_z1_plus_z2_k(k):
    """φ_k: (z_1, z_2) ↦ (z_1 + z_2^k, z_2), NOT in the parabolic
    (z_1 receives a pure-z_2 term, so (z_1) is NOT preserved).
    Symplectic check: det = ∂_{z_1}(z_1 + z_2^k) · ∂_{z_2}(z_2) -
    ∂_{z_2}(z_1 + z_2^k) · ∂_{z_1}(z_2) = 1 · 1 - k z_2^{k-1} · 0 = 1.  ✓
    """
    Phi_1 = {(1, 0): Fraction(1), (0, k): Fraction(1)}
    Phi_2 = {(0, 1): Fraction(1)}
    return Phi_1, Phi_2


# ---------------------------------------------------------------------------
# Brane-line direction: $\R \times \{0\} \subset \R^2 \times \widehat{\C^2}_0$
# is parametrized by $z_1 = 0$ projection (the cyclic vacuum is at
# $a = 0$ column, which is $z_1$-free at depth 0).
# Thus the brane-line direction is the $z_1 = 0$ formal axis, and
# $\Symp_{\rm form}$ preserving the brane line is the parabolic $P_{(z_1)}$.
# ---------------------------------------------------------------------------

def brane_line_subgroup_check(Phi_1, Phi_2):
    r"""Returns True iff $\varphi$ preserves the brane-line $z_1 = 0$,
    i.e., $\Phi_1|_{z_1 = 0} = 0$ as a polynomial in $z_2$.
    Equivalent to: $(z_1) \subset \Phi_1 \cdot \widehat A$.
    """
    return preserves_z1_ideal(Phi_1)


# ---------------------------------------------------------------------------
# SF.5_T1 — $\varphi: z_1 \mapsto z_1 + z_2^k$ — NON-parabolic test.
# Should verify functoriality of joint composite FAILS (not in parabolic),
# but the joint composite still factors at the chain level if the matrix
# factor is target-free (which it is for gl(N|N)).
# ---------------------------------------------------------------------------

def test_SF5_T1_nonparabolic():
    r"""Test $\varphi: z_1 \mapsto z_1 + z_2^k$ for k = 1, 2, 3.

    For each k:
      (i) verify $\varphi$ is symplectic (det = 1);
      (ii) verify $\varphi$ is NOT in the parabolic (z_1 ideal NOT preserved);
      (iii) verify joint composite on test cocycle $\omega(z_1, z_2)$:
            $\Lambda^{\Str} \otimes \tau_{\Symp}$ commutes pointwise on the
            matrix-factor-target-factor tensor split, regardless of
            parabolic.

    Reason: $\Lambda^{\Str}$ acts only on matrix factor; $\varphi^*$ acts
    only on target factor. They tensor-commute. The parabolic restriction
    is needed for the *m-adic completion* direction, not for the
    tensor-factor commutation.
    """
    results = []
    for k in range(1, 4):
        Phi_1, Phi_2 = phi_z1_plus_z2_k(k)
        sympl = is_symplectic(Phi_1, Phi_2, trunc_deg=8)
        in_parab = brane_line_subgroup_check(Phi_1, Phi_2)
        # Test joint composite: does φ* on ω(z_1, z_2) still give
        # a valid Symp_form transformation?
        omega_input = (
            {(1, 0): Fraction(1)},  # z_1
            {(0, 1): Fraction(1)},  # z_2
        )
        f_pull = pullback(omega_input[0], Phi_1, Phi_2, trunc_deg=6)
        g_pull = pullback(omega_input[1], Phi_1, Phi_2, trunc_deg=6)
        omega_phi = poisson(f_pull, g_pull)
        omega_orig = poisson(omega_input[0], omega_input[1])  # = {(0,0): 1}
        # ω(φ z_1, φ z_2) = ω(z_1, z_2) iff Symp invariant
        omega_diff = poly_add(omega_phi, poly_scale(omega_orig, -1))
        omega_symp_invariant = (len(omega_diff) == 0)
        results.append({
            "k": k,
            "symplectic": sympl,
            "in_parabolic": in_parab,
            "omega_symp_invariant": omega_symp_invariant,
        })
    return results


