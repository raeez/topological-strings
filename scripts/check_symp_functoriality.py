# SF.5_T6 — Queer central J commutation with Symp_form parabolic action.
#
# The queer central element $J = \begin{pmatrix} 0 & I_N \\ -I_N & 0
# \end{pmatrix}$ on q(N) lives in the matrix factor.
# Symp_form acts on the target factor $\widehat A$ only.
# By tensor-factor disjointness, $[J, \varphi^*] = 0$ for ALL
# $\varphi \in \Symp_{\rm form}$, parabolic or not.
#
# Test: verify $J \otimes \mathrm{id}_A$ commutes with
# $\mathrm{id}_g \otimes \varphi^*$ on the joint module.
# ---------------------------------------------------------------------------

def test_SF5_T6_queer_J_commutation():
    r"""$J \in \mathfrak q(N)_{\bar 1}$ commutes with $\Symp_{\rm form}$
    (full group, not just parabolic) because tensor factors are disjoint.

    Test: on the joint module $\C[z_1, z_2] \otimes \mathfrak q(N)$,
    apply $J \otimes \mathrm{id}$ then $\mathrm{id} \otimes \varphi^*$
    and reverse; confirm equality.
    """
    fails = 0
    total = 0
    log = []
    # Model: J acts on q(N) factor by multiplication (as central element);
    # phi^* acts on target factor.
    # Test vector: ((1, 0), J) [= z_1 ⊗ J]
    # Apply J (multiply by central matrix scalar c_J = N for q(N)):
    # Apply phi^*: z_1 ↦ Phi_1
    # Path A: J then phi^*: c_J · phi^*(z_1)
    # Path B: phi^* then J: c_J · phi^*(z_1)
    # Identical regardless of phi.
    for k in range(1, 4):
        for variant in ["parabolic", "non-parabolic"]:
            if variant == "parabolic":
                Phi_1, Phi_2 = phi_z2_plus_z1_k(k)
            else:
                Phi_1, Phi_2 = phi_z1_plus_z2_k(k)
            # Test: phi^* · J · v  vs  J · phi^* · v  on v = z_1 ⊗ J
            c_J = Fraction(1)  # symbolic central charge (proportional to N)
            test_vec = {(1, 0): Fraction(1)}
            # path A
            J_v = poly_scale(test_vec, c_J)
            path_A = pullback(J_v, Phi_1, Phi_2, trunc_deg=6)
            # path B
            phi_v = pullback(test_vec, Phi_1, Phi_2, trunc_deg=6)
            path_B = poly_scale(phi_v, c_J)
            diff = poly_add(path_A, poly_scale(path_B, -1))
            total += 1
            if len(diff) != 0:
                fails += 1
                log.append((k, variant, dict(diff)))
    return total, fails, log


# ---------------------------------------------------------------------------
# SF.5_T7 — Per-discharge functoriality summary.
#
# For each Phase-4 discharge, classify functoriality:
# - F'' (joint super-balanced Symp-equivariant): PARABOLIC (m-adic).
# - G^otr (queer trace anomaly): FULL Symp_form (matrix-factor only).
# - Classical-super gl/osp/psl/q DISCHARGE: PARABOLIC (matches F'').
# - Classical-super p/sl(M≠N) FAIL: SYMP-INDEPENDENT (failure not in target).
# - Decoupling-Proposition partial chiral: PARABOLIC (m-adic in z_1).
